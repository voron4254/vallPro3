        <div class="box2">
            <span>1822</span>
            <p>Семья Манн
                Предыстория семьи Манн начинается ещё в 19 веке с состоятельного англичанина <a href="https://wiki.teamfortress.com/wiki/Non-player_characters/ru#.D0.97.D0.B5.D1.84.D0.B5.D0.BD.D0.B0.D0.B9.D0.B8_.D0.9C.D0.B0.D0.BD.D0.BD" class="ss">Зефенайи Манн</a>. Зефенайя Манн был владельцем Mann & Sons Munitions Concerns(Концерн по производству боеприпасов семьи Манн) – которая в те времена была более известна под названием Zepheniah Mann & Sons Co.(предприятие Зефенайи Манн и сыновей), но сейчас именуется просто<a href="https://wiki.teamfortress.com/wiki/Mann_Co./ru" class="ss"> Манн Ко</a>. 2 сентября 1822 его жена, чьё имя Бетти, родила трёх сыновей -<a href="https://wiki.teamfortress.com/wiki/Non-player_characters/ru#.D0.91.D0.BB.D1.83.D1.82.D0.B0.D1.80.D1.85_.D0.B8_.D0.A0.D0.B5.D0.B4.D0.BC.D0.BE.D0.BD.D0.B4_.D0.9C.D0.B0.D0.BD.D0.BD.D1.8B" class="ss"> Рэдмонда</a>, <a href="https://wiki.teamfortress.com/wiki/Non-player_characters/ru#.D0.91.D0.BB.D1.83.D1.82.D0.B0.D1.80.D1.85_.D0.B8_.D0.A0.D0.B5.D0.B4.D0.BC.D0.BE.D0.BD.D0.B4_.D0.9C.D0.B0.D0.BD.D0.BD.D1.8B" class="ss">Блутарха</a> и <a href="https://wiki.teamfortress.com/wiki/Non-player_characters/ru#.D0.91.D0.BB.D1.83.D1.82.D0.B0.D1.80.D1.85_.D0.B8_.D0.A0.D0.B5.D0.B4.D0.BC.D0.BE.D0.BD.D0.B4_.D0.9C.D0.B0.D0.BD.D0.BD.D1.8B" class="ss">Грея</a>, но она, увы, скончалась при родах. Несмотря на то, что Рэдмонд и Блутарх были абсолютно нормальными, их недоношенный брат Грей научился разговаривать ещё в утробе! Такие дела. Зефенайя предрёк ребёнку скорую смерть от удушения, но он был похищен орлом во время «Великой орлиной кары» в 1822. Оставшиеся братья росли с знанием об исчезнувшем брате.
                
                Грей был взращен в любви своего похитителя. Питавшийся личинками и мышами Грей был принят другими орлами и становился с каждым днём сильнее. Когда он стал достаточно сильным, то он убил и съел орлицу вместе с её детьми. Позже он вернулся к цивилизации. Позже.
            </p>
